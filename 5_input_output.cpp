#include <iostream>
using namespace std;

int main()
{
    /*
      << is called insertion operator
      >> is called extraction operator
    */
    
    cout<<"BASIC INPUT OUTPUT IN C++ \n";
    int num1,num2;
    cout<<"enter the value of num1 ";
    cin>>num1;
    cout<<"enter the value of num2 ";
    cin>>num2;
    
    cout<<"the sum is "<<num1+num2;

    return 0;
}
